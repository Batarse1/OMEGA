-- Tablas para esquema ODS
CREATE TABLE ODS.BUSINESS_ENTITY (
    BUSINESS_ENTITY_ID NUMBER PRIMARY KEY,
    ROWGUID VARCHAR2(36),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.ADDRESS (
    ADDRESS_ID NUMBER PRIMARY KEY,
    ADDRESS_LINE1 VARCHAR2(100),
    ADDRESS_LINE2 VARCHAR2(100),
    CITY VARCHAR2(50),
    STATE_PROVINCE_ID NUMBER,
    POSTAL_CODE VARCHAR2(20),
    SPATIAL_LOCATION VARCHAR2(255),
    ROWGUID VARCHAR2(36),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.COUNTRY_REGION (
    COUNTRY_REGION_CODE CHAR(2) PRIMARY KEY,
    NAME VARCHAR2(100),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.BUSINESS_ENTITY_ADDRESS (
    BUSINESS_ENTITY_ID NUMBER,
    ADDRESS_ID NUMBER,
    ADDRESS_TYPE_ID NUMBER,
    ROWGUID VARCHAR2(36),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.EMAIL_ADDRESS (
    EMAIL_ADDRESS_ID NUMBER PRIMARY KEY,
    BUSINESS_ENTITY_ID NUMBER,
    EMAIL_ADDRESS VARCHAR2(100 CHAR) UNIQUE,
    ROWGUID VARCHAR2(50 CHAR),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.DEPARTMENT (
    DEPARTMENT_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(50 CHAR),
    GROUP_NAME VARCHAR2(50 CHAR),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.EMPLOYEE (
  BUSINESS_ENTITY_ID NUMBER PRIMARY KEY,
  NATIONAL_ID_NUMBER VARCHAR2(20),
  LOGIN_ID VARCHAR2(50),
  ORGANIZATION_NODE VARCHAR2(50),
  ORGANIZATION_LEVEL NUMBER,
  JOB_TITLE VARCHAR2(50),
  BIRTH_DATE DATE,
  MARITAL_STATUS CHAR(1),
  GENDER CHAR(1),
  HIRE_DATE DATE,
  SALARIED_FLAG NUMBER(1),
  VACATION_HOURS NUMBER,
  SICK_LEAVE_HOURS NUMBER,
  CURRENT_FLAG NUMBER(1),
  ROW_GUID VARCHAR2(50 CHAR),
  MODIFIED_DATE DATE
);

CREATE TABLE ODS.EMPLOYEE_DEPARTMENT_HISTORY (
  BUSINESS_ENTITY_ID NUMBER,
  DEPARTMENT_ID NUMBER,
  SHIFT_ID NUMBER,
  START_DATE DATE,
  END_DATE DATE,
  MODIFIED_DATE DATE
);
CREATE TABLE ODS.PRODUCT_CATEGORY (
    PRODUCT_CATEGORY_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(50 CHAR),
    ROWGUID VARCHAR2(36),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.PRODUCT_SUBCATEGORY (
    PRODUCT_SUBCATEGORY_ID NUMBER PRIMARY KEY,
    PRODUCT_CATEGORY_ID NUMBER,
    NAME VARCHAR2(50 CHAR),
    ROWGUID VARCHAR2(36),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.STATE_PROVINCE (
    STATE_PROVINCE_ID NUMBER PRIMARY KEY,
    STATE_PROVINCE_CODE VARCHAR2(20 CHAR),
    COUNTRY_REGION_CODE VARCHAR2(20 CHAR),
    IS_ONLY_STATE_PROVINCE_FLAG NUMBER,
    NAME VARCHAR2(50 CHAR),
    TERRITORY_ID NUMBER,
    ROWGUID VARCHAR2(36),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.EMPLOYEE_PAY_HISTORY (
    BUSINESS_ENTITY_ID NUMBER PRIMARY KEY,
    RATE_CHANGE_DATE DATE,
    RATE NUMBER,
    PAY_FREQUENCY NUMBER,
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.PERSON (
    BUSINESS_ENTITY_ID NUMBER PRIMARY KEY,
    PERSON_TYPE VARCHAR2(2),
    NAME_STYLE NUMBER,
    TITLE VARCHAR2(10),
    FIRST_NAME VARCHAR2(50),
    MIDDLE_NAME VARCHAR2(25),
    LAST_NAME VARCHAR2(50),
    SUFFIX VARCHAR2(10),
    EMAIL_PROMOTION NUMBER,
    ADDITIONAL_CONTACT_INFO VARCHAR2(2000),
    DEMOGRAPHICS VARCHAR2(800),
    ROWGUID VARCHAR2(36),
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.PERSON_PHONE (
    BUSINESS_ENTITY_ID NUMBER PRIMARY KEY,
    PHONE_NUMBER VARCHAR2(25),
    PHONE_NUMBER_TYPE_ID NUMBER,
    MODIFIED_DATE DATE
);

CREATE TABLE ODS.PRODUCT (
    PRODUCT_ID NUMBER,
    NAME VARCHAR2(260),
    PRODUCT_NUMBER VARCHAR2(50),
    MAKE_FLAG NUMBER,
    FINISHED_GOODS_FLAG NUMBER,
    COLOR VARCHAR2(50),
    SAFETY_STOCK_LEVEL NUMBER,
    REORDER_POINT NUMBER,
    STANDARD_COST NUMBER,
    LIST_PRICE NUMBER,
    SIZE_ VARCHAR2(50),
    SIZE_UNIT_MEASURE_CODE VARCHAR2(50),
    WEIGHT_UNIT_MEASURE_CODE VARCHAR2(50),
    WEIGHT NUMBER,
    DAYS_TO_MANUFACTURE NUMBER,
    PRODUCT_LINE VARCHAR2(50),
    CLASS VARCHAR2(50),
    STYLE VARCHAR2(50),
    PRODUCT_SUBCATEGORY_ID NUMBER,
    PRODUCT_MODEL_ID NUMBER,
    SELL_START_DATE DATE,
    SELL_END_DATE DATE,
    DISCONTINUED_DATE DATE,
    ROWGUID VARCHAR2(50),
    MODIFIED_DATE DATE
);


-- Tablas para esquema DATA_WAREHOUSE
CREATE TABLE DATA_WAREHOUSE.CUSTOMERS (
    CUSTOMER_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(255),
    EMAILADDRESS VARCHAR2(100) UNIQUE,
    PHONENUMBER VARCHAR2(25),
    ADDRESSLINE1 VARCHAR2(200),
    CITY VARCHAR2(50),
    POSTALCODE VARCHAR2(10),
    STATE VARCHAR2(100),
    COUNTRY VARCHAR2(100),
    LOADING_DATE DATE
);

CREATE TABLE DATA_WAREHOUSE.PRODUCTS (
    PRODUCT_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(255),
    COLOR VARCHAR2(50),
    LISTPRICE NUMBER,
    SUBCATEGORY VARCHAR2(100),
    CATEGORY VARCHAR2(100),
    LOADING_DATE DATE
);

CREATE TABLE DATA_WAREHOUSE.EMPLOYEES (
    EMPLOYEE_ID NUMBER PRIMARY KEY,
    JOBTITLE VARCHAR2(100),
    GROUPNAME VARCHAR2(100),
    RATE NUMBER,
    QUANTITY NUMBER,
    LOADING_DATE DATE
);
