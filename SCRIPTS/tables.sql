CREATE TABLE EMAIL_ADDRESS (
    EMAIL_ADDRESS_ID NUMBER PRIMARY KEY,
    BUSINESS_ENTITY_ID NUMBER,
    EMAIL_ADDRESS VARCHAR2(100 CHAR) UNIQUE,
    ROWGUID VARCHAR2(50 CHAR),
    MODIFIED_DATE DATE
);

CREATE TABLE DEPARTMENT (
    DEPARTMENT_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(50 CHAR),
    GROUP_NAME VARCHAR2(50 CHAR),
    MODIFIED_DATE DATE
);

CREATE TABLE PRODUCT_CATEGORY (
    PRODUCT_CATEGORY_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(50 CHAR),
    ROWGUID RAW(16),
    MODIFIED_DATE DATE
);

CREATE TABLE PRODUCT_SUBCATEGORY (
    PRODUCT_SUBCATEGORY_ID NUMBER PRIMARY KEY,
    PRODUCT_CATEGORY_ID NUMBER,
    NAME VARCHAR2(50 CHAR),
    ROWGUID RAW(16),
    MODIFIED_DATE DATE,
    FOREIGN KEY (PRODUCT_CATEGORY_ID) REFERENCES PRODUCT_CATEGORY(PRODUCT_CATEGORY_ID)
);

CREATE TABLE STATE_PROVINCE (
    STATE_PROVINCE_ID NUMBER PRIMARY KEY,
    STATE_PROVINCE_CODE VARCHAR2(20 CHAR),
    COUNTRY_REGION_CODE VARCHAR2(20 CHAR),
    IS_ONLY_STATE_PROVINCE_FLAG NUMBER,
    NAME VARCHAR2(50 CHAR),
    TERRITORY_ID NUMBER,
    ROWGUID RAW(16),
    MODIFIED_DATE DATE
);

CREATE TABLE EMPLOYEE_PAY_HISTORY (
    BusinessEntityID NUMBER PRIMARY KEY,
    RateChangeDate	 DATE,
    Rate NUMBER,
    PayFrequency NUMBER,
    ModifiedDate DATE
);

CREATE TABLE PERSON(
  BusinessEntityID NUMBER PRIMARY KEY,
  PersonType VARCHAR2(2),
  NameStyle NUMBER,
  Title VARCHAR2(10),
  FirstName VARCHAR2(50),
  MiddleName VARCHAR2(25),
  LastName VARCHAR2(50),
  Suffix VARCHAR2(10),
  EmailPromotion NUMBER,
  AdditionalContactInfo VARCHAR2(200),
  Demographics VARCHAR2(200),
  Rowguid VARCHAR(36),
  ModifiedDate DATE
);

CREATE TABLE PERSON_PHONE(
    BusinessEntityID NUMBER PRIMARY KEY,
    PhoneNumber VARCHAR2(25),
    PhoneNumberTypeID NUMBER,
    ModifiedDate DATE
);

CREATE TABLE PRODUCT(
    ProductID NUMBER,
    Name VARCHAR2(260),
    ProductNumber VARCHAR2(50),
    MakeFlag NUMBER,
    FinishedGoodsFlag NUMBER,
    Color VARCHAR2(50),
    SafetyStockLevel NUMBER,
    ReorderPoint NUMBER,
    StandardCost NUMBER,
    ListPrice NUMBER,
    Size_ VARCHAR2(50),
    SizeUnitMeasureCode VARCHAR2(10),
    WeightUnitMeasureCode VARCHAR2(10),
    Weight NUMBER,
    DaysToManufacture NUMBER,
    ProductLine VARCHAR2(10),
    Class VARCHAR2(10),
    Style VARCHAR2(10),
    ProductSubcategoryID NUMBER,
    ProductModelID NUMBER,
    SellStartDate DATE,
    SellEndDate DATE,
    DiscontinuedDate DATE,
    Rowguid  VARCHAR2(16),
    ModifiedDate DATE
);
